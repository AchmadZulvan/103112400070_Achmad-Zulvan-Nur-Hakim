#include <iostream>
using namespace std;
int main(){
    float x1,x2;
    cout << "Masukaan angka pertama :";
    cin >> x1;
    cout << "Masukaan angka kedua :";
    cin >> x2;
    cout << "Hasil Penjumlahan :" << x1 + x2 << endl;
    cout << "Hasil Pengurangan :" << x1 - x2 << endl;
    cout << "Hasil Perkalian :" << x1 * x2 << endl;
    cout << "Hasil Pembagian :" << x1 / x2 << endl;
}